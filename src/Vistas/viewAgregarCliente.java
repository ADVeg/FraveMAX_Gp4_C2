    private void jBotEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBotEnviarActionPerformed
        boolean msj=false;
        if(!msj){msj=verificarCampos(jTextNom);}
        if(!msj){msj=verificarCampos(jTextDom);}
        if(!msj){msj=verificarCampos(jTextTel);}
        
        if(!msj){
            Vista_FraveMAX.clien=new Cliente((jTextNom.getText()),jTextDom.getText(),jTextTel.getText(),true);
            Vista_FraveMAX.cliD.agregarCliente(Vista_FraveMAX.clien);
            Vista_FraveMAX.clien=null;
            Limpiar();
        }
    }//GEN-LAST:event_jBotEnviarActionPerformed

